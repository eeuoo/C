#include <stdint.h>

int main()
{
    int a = 99;
    int b = 2;

    printf(" a + b의 결과 : %d\n", a + b);
    printf(" a - b의 결과 : %d\n", a - b);
    printf(" a * b의 결과 : %d\n", a * b);
    printf(" a / b의 결과 : %d\n", a / b);
    printf(" a %% b의 결과 : %d\n", a % b);
    printf(" -a의 결과 : %d\n", -a);

    return 0;
}